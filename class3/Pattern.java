class Pattern{
	public static void main(String[] args){
		//1st loop for the spacing after the *
	for(int i=1;i<5;i++){
		//Second loop for the printing of the stars
		for(int j=1;j<=i;j++){
			System.out.print("*");
			//using print instead of println we get to print the star in the same line 
			//rather moving to the new line
		}
		System.out.println("\n");
		//changing line 
		
}
}
}